void ArquivoController::save(Request &request, StreamResponse &response)
{
    Json::Value result;
    result["operation"] = "arquivo_save";
    UsuarioPtr usuario = getSession(request, response).get<UsuarioPtr>("usuario");
    if(usuario){
        int idNovoArquivo=0;
        request.handleUploads();
        const string& materia_id = request.get("id");
        if( ! materia_id.empty() ){
            MateriaPtr materia(new Materia(stoi(materia_id)));
            for (UploadFile& file: request.uploadFiles)
            {
                if(file.getData().empty() || file.getName().empty())
                    continue;
                idNovoArquivo = model.save(usuario, materia, file.getName(), file.getData());
            }
        }
        if(idNovoArquivo){
//            result["data"] = to_string(recado->getData());
            result["success"] = true;
        }
        else
            result["success"] = false;
    }else{
        result["success"] = false;
        result["erro_msg"] = "NÃ£o permitido";
    }

    response << result;
}

void ArquivoController::list(Request &request, StreamResponse &response)
{
    Json::Value result;
    result["operation"] = "arquivo_list";
    result["success"] = false;
    UsuarioPtr usuario = getSession(request, response).get<UsuarioPtr>("usuario");
    if(usuario){
        Json::Value item;
        const string& materia_id = request.get("materia_id");
        ArquivoList lista = model.list(materia_id);
        for(ArquivoPtr arquivo: lista)
        {
            const string& nome = arquivo->getPath();
            item["nome"] = nome.substr(nome.rfind('/')+1);
            item["path"] = arquivo->getPath();
            item["data"] = to_string(arquivo->getData());
            item["usuario"] = arquivo->getUsuario()->getNome();
            result["arquivos"].append(item);
        }
        if( ! lista.empty())
            result["success"] = true;
    }
    response << result;
}

void ArquivoController::setup()
{
    addRoute("POST", "/arquivo/save", ArquivoController, save);
    addRoute("GET", "/arquivo/list", ArquivoController, list);
}

void ArquivoController::setDirStorage(string path)
{
    model.setDirStorage(path);
}

